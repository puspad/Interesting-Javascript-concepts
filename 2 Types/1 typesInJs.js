// There are 7 types in JS

// Primitive types
console.log(typeof 5); // number
console.log(typeof true); // boolean
console.log(typeof 'Hello there'); // string
console.log(typeof undefined); // undefined
console.log(typeof null); // Null is of type object, this is mistake in JS
console.log(typeof Symbol('just symbol')); // Symbol - related to object properties(read more on this. new introduction in es6)

// Non-Primitive types
console.log(typeof {}); // object
console.log(typeof function(){}); // Type of returns function, but function is also of type object {}
